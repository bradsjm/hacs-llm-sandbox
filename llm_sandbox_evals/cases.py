"""Authored eval cases loaded from the native Pydantic Evals dataset file.

The source of truth is ``data/cases.yaml``. Its adjacent
``data/cases_schema.json`` sidecar is generated by ``Dataset.to_file()`` and
keeps case authoring aligned with the native Pydantic Evals file format.
"""

from pathlib import Path

from pydantic_evals import Dataset

from llm_sandbox_evals.schema import EvalCase

_CASES_PATH = Path(__file__).parent / "data" / "cases.yaml"


def load_cases() -> list[EvalCase]:
    """Load authored cases with Pydantic Evals as the dataset-file authority."""
    dataset = Dataset[EvalCase, object, object].from_file(_CASES_PATH)
    return [case.inputs for case in dataset.cases]


# Stable public surface for existing matrix execution and CLI selection code.
CASES = load_cases()
